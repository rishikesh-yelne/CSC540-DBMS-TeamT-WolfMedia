package edu.ncsu.csc540.s23.backend.model;

public class GuestSpeaker {

    private Long guestSpeakerId;

    private String guestSpeakerFirstName;

    private String guestSpeakerLastName;

    private String guestSpeakerPhoneNum;

    private String guestSpeakerEmail;

    private String guestSpeakerCity;

    public Long getGuestSpeakerId() {
        return guestSpeakerId;
    }

    public void setGuestSpeakerId(Long guestSpeakerId) {
        this.guestSpeakerId = guestSpeakerId;
    }

    public String getGuestSpeakerFirstName() {
        return guestSpeakerFirstName;
    }

    public void setGuestSpeakerFirstName(String guestSpeakerFirstName) {
        this.guestSpeakerFirstName = guestSpeakerFirstName;
    }

    public String getGuestSpeakerLastName() {
        return guestSpeakerLastName;
    }

    public void setGuestSpeakerLastName(String guestSpeakerLastName) {
        this.guestSpeakerLastName = guestSpeakerLastName;
    }

    public String getGuestSpeakerPhoneNum() {
        return guestSpeakerPhoneNum;
    }

    public void setGuestSpeakerPhoneNum(String guestSpeakerPhoneNum) {
        this.guestSpeakerPhoneNum = guestSpeakerPhoneNum;
    }

    public String getGuestSpeakerEmail() {
        return guestSpeakerEmail;
    }

    public void setGuestSpeakerEmail(String guestSpeakerEmail) {
        this.guestSpeakerEmail = guestSpeakerEmail;
    }

    public String getGuestSpeakerCity() {
        return guestSpeakerCity;
    }

    public void setGuestSpeakerCity(String guestSpeakerCity) {
        this.guestSpeakerCity = guestSpeakerCity;
    }
}
